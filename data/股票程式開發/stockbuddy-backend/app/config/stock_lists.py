def get_all_presets() -> dict:
    """取得所有預設清單資訊"""
    return {
        key: {
            "name": val["name"],
            "description": val["description"],
            "count": len(val["stocks"]),
            "estimated_time": val["estimated_time"],
        }
        for key, val in STOCK_PRESETS.items()
    }
