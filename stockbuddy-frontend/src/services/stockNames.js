/**
 * 取得股票中文名稱
 * @param {string} stockId - 股票代號
 * @returns {string} 中文名稱或原代號
 */
export const getStockName = (stockId) => {
  return STOCK_NAMES[stockId] || stockId;
};

/**
 * 取得股票完整顯示名稱 (代號 + 名稱)
 * @param {string} stockId - 股票代號
 * @returns {string} 完整名稱如 "2330 台積電"
 */
export const getStockFullName = (stockId) => {
  const name = STOCK_NAMES[stockId];
  return name ? `${stockId} ${name}` : stockId;
};

/**
 * 批量取得股票名稱
 * @param {string[]} stockIds - 股票代號陣列
 * @returns {Object} { stockId: name } 對照表
 */
export const getStockNames = (stockIds) => {
  const result = {};
  stockIds.forEach((id) => {
    result[id] = getStockName(id);
  });
  return result;
};

export default {
  STOCK_NAMES,
  getStockName,
  getStockFullName,
  getStockNames,
};
